(BOUCLEPIXEL) // boucle servant à générer des lignes noires l'une au dessus de l'autre 

@position 
A=M
M=-1 // on assigne -1 à l'addresse encodée dans position, ce qui crée une ligne de pixels noirs

@32
D=A
@position
M=M+D // on ajoute 32 à la position pour passer à la ligne suivante
@hauteurpixel
M=M-1
D=M // à chaque répétition de boucle on enlève 1 à hauteurpixel
@BOUCLEPIXEL // quand hauteurpixel atteint 0 on sort de la boucle
D;JGT

@count // on ajoute 1 à count quand on sort la boucle
M=M+1

@UN // on jump à UN à chaque fois qu'on sort de la boucle
0;JMP

(FIN)
